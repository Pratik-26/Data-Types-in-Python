# Integer Data Type
a = 5
print("Value of a:", a)
print("Data type of a:", type(a))

print()

# Float Data Type
b = 2.5
print("Value of b:", b)
print("Data type of b:", type(b))

print()

# String Data Type
c = "Codingal"
print("Value of c:", c)
print("Data type of c:", type(c))

print()

# Boolean Data Type
d = True
print("Value of d:", d)
print("Data type of d:", type(d))
